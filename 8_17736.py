# Семеричные семизначные числа в base 7: цифры от 0 до 6
# d1 (первая цифра): 1,2,4,6  → 4 варианта (не 0, не 3, не 5)
# d7 (последняя цифра): 1,3,5 → 3 варианта (нечётные: не 0,2,4,6)
# d2..d6: 0–6, но всего нулей в числе ≤ 1 (и нули возможны только здесь)

# Случай 1: нулей вообще нет
# d2..d6: каждый из 1–6 → 6 вариантов на позицию
no_zeros = 4 * (6 ** 5) * 3
# 4 * 7776 * 3 = 93312

# Случай 2: ровно один нуль (в одной из позиций d2–d6)
# Выбор позиции для 0: 5 вариантов
# Остальные 4 позиции из d2–d6: каждый 1–6 → 6^4
one_zero = 4 * (5 * (6 ** 4)) * 3
# 4 * (5 * 1296) * 3 = 4 * 6480 * 3 = 77760

total = no_zeros + one_zero
print(total)  # 171072
