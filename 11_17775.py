# Минимальная мощность алфавита, чтобы 18750 номеров по 132 символа
# занимали больше 4 Мбайт при минимальном посимвольном кодировании

N = 18750          # количество номеров
L = 132            # длина каждого номера в символах
M = 4 * 1024 * 1024  # 4 Мбайт в байтах

# Перебираем k — мощность алфавита, начиная с минимально разумного (2)
for k in range(2, 100000):
    # Сколько бит нужно на один символ
    # ceil(log2(k)) — минимальное число бит, чтобы закодировать k символов
    import math
    bits_per_char = math.ceil(math.log2(k))

    # Общий объём в битах на все номера
    total_bits = N * L * bits_per_char

    # В байтах
    total_bytes = total_bits // 8

    # Условие: больше 4 Мбайт
    if total_bytes > M:
        print(k)  # это минимальное k
        break
